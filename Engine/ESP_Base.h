struct Vec3
{
	float x, y, z;

	float GetDistance(Vec3 end)
	{
		float X = x - end.x;
		float Y = y - end.y;
		float Z = z - end.z;

		return (float)sqrt(X * X + Y * Y + Z * Z);
	}
};

